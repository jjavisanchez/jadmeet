function page(ctx)
{
	ctx.save();
    ctx.beginPath();
    ctx.moveTo(0,0);
    ctx.lineTo(129,0);
    ctx.lineTo(129,86);
    ctx.lineTo(0,86);
    ctx.closePath();
    ctx.clip();
    ctx.strokeStyle = 'rgba(0,0,0,0)';
    ctx.lineCap = 'butt';
    ctx.lineJoin = 'miter';
    ctx.miterLimit = 4;
    ctx.save();
    ctx.fillStyle = "#ffffff";
    ctx.strokeStyle = "#000000";
    ctx.lineWidth = 1.25;
    ctx.beginPath();
    ctx.moveTo(0,0);
    ctx.lineTo(129,0);
    ctx.quadraticCurveTo(129,0,129,0);
    ctx.lineTo(129,86);
    ctx.quadraticCurveTo(129,86,129,86);
    ctx.lineTo(0,86);
    ctx.quadraticCurveTo(0,86,0,86);
    ctx.lineTo(0,0);
    ctx.quadraticCurveTo(0,0,0,0);
    ctx.closePath();
    ctx.fill();
    ctx.stroke();
    ctx.restore();
    ctx.restore();
}

